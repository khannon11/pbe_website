        With a dedication to enriching the academic, cultural, and social pursuits of
        its members, PBE shapes the lives of its actives and enjoys the continued
        support of hundreds of alumni. The current brotherhood comes from a wide
        range of geographic and cultural backgrounds and is engaged in numerous
        majors, sports, music and cultural groups, and other extracurricular
        activities. Welcome to Phi Beta Epsilon.
